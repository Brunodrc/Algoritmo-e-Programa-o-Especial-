def main():
    #ENTRADA
    valorA = int(input("Digite um número: "))
    valorB = int(input("Digite um número: "))
    valorC = int(input("Digite um número: "))
    valorD = int(input("Digite um número: "))

    #processamento

    diferenca = (valorA * valorB) - (valorC * valorD)

    #SAIDA

    print(f"DIFERENCA = {diferenca}")

main()